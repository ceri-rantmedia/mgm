package uk.co.cerihughes.denon.core.dao.impl.dlna;


public class ContainerData
{
	private String playlistContainerId;
	private String artistContainerId;
	private String albumContainerId;
	private String trackContainerId;

	public String getPlaylistContainerId()
	{
		return playlistContainerId;
	}

	public void setPlaylistContainerId(String playlistContainerId)
	{
		this.playlistContainerId = playlistContainerId;
	}

	public String getArtistContainerId()
	{
		return artistContainerId;
	}

	public void setArtistContainerId(String artistContainerId)
	{
		this.artistContainerId = artistContainerId;
	}

	public String getAlbumContainerId()
	{
		return albumContainerId;
	}

	public void setAlbumContainerId(String albumContainerId)
	{
		this.albumContainerId = albumContainerId;
	}

	public String getTrackContainerId()
	{
		return trackContainerId;
	}

	public void setTrackContainerId(String trackContainerId)
	{
		this.trackContainerId = trackContainerId;
	}

}
